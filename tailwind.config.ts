const formatFonts = (): TailwindFonts => {
  const entries = Object.entries(fonts)

  const data = entries.map(([name, object]) => {
    const sizes = Object.entries(object)
    const sizesEntries = sizes.map(([size, { fontSize, ...rest }]) => {
      return [[`${name}-${size}`], [fontSize, rest]]
    })
    return sizesEntries;
  })

  const formattedFonts = Object.fromEntries(data.flat())
  return formattedFonts
}

